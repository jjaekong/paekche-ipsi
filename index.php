    <script>
        (function($) {
            
            // 학과영역 슬라이드
            var departSlider = $('.departs .depart-list > ul').bxSlider({
                controls: false,
                pager: false
            });
            $(document).on('click', '.departs .depart-list nav .btn-prev', function(e) {
                departSlider.goToPrevSlide();
                e.preventDefault();
            });
            $(document).on('click', '.departs .depart-list nav .btn-next', function(e) {
                departSlider.goToNextSlide();
                e.preventDefault();
            });
            
            // 전체학과 보기 버튼
            $(document).on('click', '.departs .depart-list .btn-all', function(e) {
                $('.departs .depart-all')
                    .show()
                    .find('> ul > li:eq('+ departSlider.getCurrentSlide() +') > a')
                    .focus();
                e.preventDefault();
            });
            
            // 전체학과 닫기 버튼
            $(document).on('click', '.departs .depart-all .btn-close', function(e) {
                $('.departs .depart-all').hide();
                $('.departs .depart-list .btn-all').focus();
                e.preventDefault();
            });
            
            // 전체학과에서 선택 시
            $(document).on('click', '.departs .depart-all ul > li > a', function(e) {
                var clickedIndex = $(this).parent().index();
                departSlider.goToSlide(clickedIndex);
                $('.departs .depart-all').hide();
                $('.departs .depart-list ul > li:eq('+(clickedIndex+1)+')').focus();
                e.preventDefault();
            });
        })(jQuery);
    </script>
